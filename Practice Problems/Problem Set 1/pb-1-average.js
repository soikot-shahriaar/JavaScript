// Find Average of 5 numbers
// 75.25, 65, 80, 99, 50
// Mathematics, Biology, Chemistry, Physics, Bangla

let mathMarks = 75.35;
let bioMarks = 65;
let chemMarks = 80;
let phyMarks = 99;
let banMarks = 50;

let averageMarks = (mathMarks + bioMarks + chemMarks + phyMarks + banMarks) / 5;

let avgResult = averageMarks.toFixed(.2);
console.log(avgResult);
