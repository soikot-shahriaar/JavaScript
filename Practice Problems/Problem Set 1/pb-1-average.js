// Find Average of 5 numbers
// 75.25, 65, 80, 99, 50
// Mathematics, Biology, Chemistry, Physics, Bangla

var mathMarks = 75.35;
var bioMarks = 65;
var chemMarks = 80;
var phyMarks = 99;
var banMarks = 50;

var averageMarks = (mathMarks + bioMarks + chemMarks + phyMarks + banMarks) / 5;

var avgResult = averageMarks.toFixed(.2);
console.log(avgResult);
